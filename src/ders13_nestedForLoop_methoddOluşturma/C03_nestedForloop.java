package ders13_nestedForLoop_methoddOluşturma;

public class C03_nestedForloop {
    public static void main(String[] args) {
        //1-4 arasındaki sayilar icin carpim tablosu olusturalim

        for (int i = 1; i <=10; i++) {

            for (int j = 1; j <=10 ; j++) {


                System.out.print(i*j + " ");

            }

            System.out.println(" ");

        }






    }
}
