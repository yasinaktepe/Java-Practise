package ders15_overLoading_whileLoop;

public class C03_whileLoop {
    public static void main(String[] args) {

        //2 basamaklı sayilari aralarinda bir boşluk olacak sekilde yanyana yazdirin

        for (int i = 10; i <100 ; i++) {
            System.out.print(i+ " ");
        }

        System.out.println("");

        //while loop ile yaparsak

        int a=10;
        while (a<100){
            System.out.print(a+ " ");
            a++;
        }












    }
}
