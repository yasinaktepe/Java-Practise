package ders04_matematilselislemler_icrementdecrement;

public class C06_Pre_PostincrementDecrement {
    public static void main(String[] args) {

        int a= 10;

        //a variableının degerini yazdirp sonra a nın degerinin 1 arttirin

        System.out.println("a : " +a); //10
        a++;

        int b=10;

        b++;

        System.out.println("b : " +b);
        //a variablenin degerinin 1 artıtıp sonra arttirin

        int c= 10;

        System.out.println("c : " + c++);   //10

        int d=10;

        System.out.println("d :" + ++d);  //11

        /*
        c++ veya ++d yi yazdirma veya atama isleminde kullanirsaniz
         c++ post increment denir önce islemi yapar sonra arttirma
         ++d pre invrement denir önce artıma sonra yazdırma
         c de d de bir artmistır.
         */


















































    }
}
